package com.lkh.ht.carrental.vo;

public class Dept {
	private long deptId;
	private String deptName;
	private long deptPhtone;
	private String deptAddr;
	/**
	 * @return the deptId
	 */
	public long getDeptId() {
		return deptId;
	}
	/**
	 * @param deptId the deptId to set
	 */
	public void setDeptId(long deptId) {
		this.deptId = deptId;
	}
	/**
	 * @return the deptName
	 */
	public String getDeptName() {
		return deptName;
	}
	/**
	 * @param deptName the deptName to set
	 */
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	/**
	 * @return the deptPhtone
	 */
	public long getDeptPhtone() {
		return deptPhtone;
	}
	/**
	 * @param deptPhtone the deptPhtone to set
	 */
	public void setDeptPhtone(long deptPhtone) {
		this.deptPhtone = deptPhtone;
	}
	/**
	 * @return the deptAddr
	 */
	public String getDeptAddr() {
		return deptAddr;
	}
	/**
	 * @param deptAddr the deptAddr to set
	 */
	public void setDeptAddr(String deptAddr) {
		this.deptAddr = deptAddr;
	}
}
